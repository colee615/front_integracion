export default (_, inject) => {
  const translate = (key, replacements = {}, locale = 'es') => {
    const lang = messages[locale] ? locale : 'es'
    let text = messages[lang][key] || messages.es[key] || key

    Object.entries(replacements).forEach(([replacementKey, value]) => {
      text = text.replace(`{${replacementKey}}`, value)
    })

    return text
  }

  inject('portalT', translate)
}
